#include "../../libft.h"

int	ft_putnbr_fd(int n, int fd)
{
	char	str;
	int		count;

	count = 0;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
		count++;
	}
	if (n > 9)
		count += ft_putnbr_fd(n / 10, fd);
	str = '0' + (n % 10);
	write(fd, &str, 1);
	count++;
	return (count);
}
